% validate_time_step.m
%
% Time-step sensitivity test.
%
% Build the traveling wave once, then vary dt.

clear;
clc;
addpath(fullfile(fileparts(mfilename('fullpath')), '..', 'src'))

%% fixed problem parameters

params.sh_target = 2e-2;
params.L         = 16;
params.k         = pi/16;
params.n_periods = 1000;
params.n_repeats = 3;
params.dx        = 1.0;

params.M         = 4;
params.align_tol = 1e-10;

params.phip = @(s) s + s.^3;
params.Phi  = @(s) 0.5*s.^2 + 0.25*s.^4;

params.newton_tol   = [1e-12, 1e-8];
params.newton_parms = [40, 40, 0.9, 3, 20];

dt_list = [5e-3, 2.5e-3, 1.25e-3, 6.25e-4];

%% build traveling wave once

% reuse the same base wave across the dt sweep
tw = build_traveling_wave_frac(params);

%% run sweep

outs = struct([]);

for i = 1:numel(dt_list)

    params.dt = dt_list(i);

    fprintf('\n=== validate_time_step: dt = %.6g ===\n', params.dt);

    out = run_phase_drift_from_tw_frac(tw, params);

    if i == 1
        outs = out;
    else
        % keep a consistent field order across runs
        outs(i) = orderfields(out, outs(1));
    end
end

%% summary table

T = build_summary_table(outs);

%% save

save('validate_time_step_results.mat', 'outs', 'T', 'dt_list', 'params');
writetable(T, 'validate_time_step_results.csv');

fprintf('\nWrote validate_time_step_results.mat\n');
fprintf('Wrote validate_time_step_results.csv\n');

fprintf('\nTime-step sweep summary:\n');
disp(T(:, {'dt', 'delta_c_driftOnly', 'delta_c_se', 'delta_c_r2', ...
           'tw_res_rel', 'E_align_max', 'E_direct_max', 'energy_rel_max'}));

fprintf(['\nNote: direct error can become order one over long times because the wave\n' ...
         'drifts far in phase relative to the fixed reference, even when the\n' ...
         'aligned error remains small.\n\n']);

%% local helper

function T = build_summary_table(outs)

summary_fields = { ...
    'dt', ...
    'n_repeats', ...
    'M', ...
    'align_tol', ...
    'c_ref', ...
    'tw_res_rel', ...
    'nsoli_ierr', ...
    'delta_c_raw', ...
    'delta_c_driftOnly', ...
    'delta_c_raw_se', ...
    'delta_c_raw_r2', ...
    'delta_c_se', ...
    'delta_c_r2', ...
    'delta_c_first_half_raw', ...
    'delta_c_second_half_raw', ...
    'delta_c_first_half_driftOnly', ...
    'delta_c_second_half_driftOnly', ...
    'corr_shift_Ealign_raw', ...
    'corr_dshift_Ealign_raw', ...
    'corr_shift_Ealign_driftOnly', ...
    'corr_dshift_Ealign_driftOnly', ...
    'E_align_max', ...
    'E_direct_max', ...
    'energy_rel_max', ...
    'omega_max_eq', ...
    'dt_omega_eq', ...
    'dt_omega_eq_over_pi', ...
    'omega_eff0', ...
    'omega_eff_max', ...
    'dt_omega_eff0', ...
    'dt_omega_eff_max', ...
    'dt_omega_eff0_over_pi', ...
    'dt_omega_eff_max_over_pi', ...
    'T_period', ...
    'steps', ...
    'store_every', ...
    'dt_sample' ...
    };

rows = repmat(struct(), numel(outs), 1);

for j = 1:numel(outs)
    for k = 1:numel(summary_fields)
        f = summary_fields{k};
        if isfield(outs(j), f)
            rows(j).(f) = outs(j).(f);
        else
            % keep missing diagnostics explicit in the table
            rows(j).(f) = NaN;
        end
    end
end

T = struct2table(rows);

end
